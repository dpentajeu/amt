                <h3>Why Do Exchange Rates Change?</h3>
                <p>Currencies trade on  an open market, just like stocks, bonds, computers, cars, and many other goods  and services. A currency's value fluctuates as its supply and demand  fluctuates, just like anything else.</p>
                <ul class="standard has-ico list-ico-sm">
                    <li><i class="ico ico-simple-dark-gray ico-arrow-right-2-sm"></i>An increase in supply or a decrease in demand for a currency can cause the value of that currency to fall. </li>
                    <li><i class="ico ico-simple-dark-gray ico-arrow-right-2-sm"></i>A decrease in the supply or an increase in demand for a currency can cause the value of that currency to rise.</li>
                </ul>
                <p>A big benefit to forex trading is that you can buy or sell any currency pair, at any time subject to available liquidity.  So if you think the Eurozone is going to break apart, you can sell the euro and buy the dollar (sell EUR/USD).  If you think the price of gold is going to go  up, based  on historical correlation patterns you can buy the  Australian dollar and sell the U.S. dollar (buy AUD/USD).</p>
                <p>This also means that there really is no such thing as a "bear market," in the traditional sense.  You can make (or lose) money when the market is trending up and down.</p>

