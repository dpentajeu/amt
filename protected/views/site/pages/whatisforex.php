                <h3>What Am I Doing When I Trade Forex?</h3>
                <p>Forex is a commonly used abbreviation for "foreign exchange," and it is typically used to describe trading in the foreign exchange market by investors and speculators.</p>
                <p> For example, imagine a situation where the U.S. dollar is expected to weaken in value relative to the euro.  A forex trader in this situation will sell dollars and buy euros.  If the euro strengthens, the purchasing power to buy dollars has now increased.  The trader can now buy back more dollars than they had to begin with, making a profit.</p>
                <p> This is similar to stock trading.  A stock trader will buy a stock if they think its price will rise in the future and sell a stock if they think its price will fall in the future.  Similarly, a forex trader will buy a currency pair if they expect its exchange rate will rise in the future and sell a currency pair if they expect its exchange rate will fall in the future. </p>
                <h3>What Is An Exchange Rate?</h3>
                <p>The foreign exchange market  is a global decentralized marketplace that determines the relative values of  different currencies. Unlike other markets, there is no centralized depository  or exchange where transactions are conducted.&nbsp;Instead, these transactions  are conducted by several market participants in several locations.&nbsp;It is  rare that any two currencies will be identical to one another in value, and  it's also rare that any two currencies will maintain the same relative value  for more than a short period of time.&nbsp; In  forex, the exchange rate between two currencies constantly changes.</p>
                <p>For example, on  January 3, 2011, one euro was worth about $1.33.&nbsp; By May 3, 2011, one euro was worth about  $1.48.&nbsp; The euro increased in value by  about 10% relative to the U.S. dollar during this time.</p>
